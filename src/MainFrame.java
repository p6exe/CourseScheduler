    private void selectSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectSubmitActionPerformed
        // TODO add your handling code here:

        //gets the current coursecodes and student objects
        CourseEntry currentCourse;
        StudentEntry currentStudent;
        ArrayList<CourseEntry> Courses = CourseQueries.getAllCourses(currentSemester);
        ArrayList<StudentEntry> Students = StudentQueries.getAllStudents();
        currentCourse = Courses.get(selectCourseComboBox.getSelectedIndex());
        currentStudent = Students.get(selectStudentComboBox.getSelectedIndex());

        //checks if the student is already enrolled in this course
        ArrayList<ScheduleEntry> StudentSchedules = ScheduleQueries.getScheduleByStudent(currentSemester, currentStudent.getStudentID());
        for (int i = 0; i < StudentSchedules.size(); i++) {
            if(currentCourse.getCoursecode().equals(StudentSchedules.get(i).getCoursecode())) {
                selectStatusLabel.setText(currentStudent.getFirstname() + currentStudent.getLastname() + " already has this class");
                return;
            }
        }

        //adds the schdeule with the student
        if(currentStudent != null && currentCourse != null){
            //waitlist
            if (CourseQueries.getCourseSeats(currentSemester,currentCourse.getCoursecode()) <= ScheduleQueries.getScheduledStudentCount(currentSemester, currentCourse.getCoursecode())) {
                selectStatusLabel.setText(currentStudent.getFirstname() + " " + currentStudent.getLastname() + " has been waitlisted for the class");
                ScheduleQueries.addScheduleEntry(new ScheduleEntry(currentSemester,currentCourse.getCoursecode(), currentStudent.getStudentID(), "W", new Timestamp(System.currentTimeMillis()) ));
            } else { //not waitlist
                selectStatusLabel.setText(currentStudent.getFirstname() + " " + currentStudent.getLastname() + " has been Scheduled for the class");
                ScheduleQueries.addScheduleEntry(new ScheduleEntry(currentSemester, currentCourse.getCoursecode(), currentStudent.getStudentID(), "S", new Timestamp(System.currentTimeMillis())));
            }

        } else {
            selectStatusLabel.setText("Can't add Course or Student, please change choose a Student or Course");
        }
    }//GEN-LAST:event_selectSubmitActionPerformed

