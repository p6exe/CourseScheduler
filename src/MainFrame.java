    private void DropCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropCourseButtonActionPerformed
        DropCourseTextArea.setText("");
        ArrayList<String> Courses = CourseQueries.getAllCourseCodes(currentSemester);
        String currentCourse = Courses.get(DropCourseComboBox.getSelectedIndex());
        
        //checks if there are no courses
        if(Courses.size() < 1) {
            DropCourseTextArea.setText("There are no course to remove");
            return;
        }
        
        ArrayList<ScheduleEntry> scheduledstudents = ScheduleQueries.getScheduledStudentsByCourse(currentSemester, currentCourse);
        ArrayList<ScheduleEntry> waitlistedstudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, currentCourse);
        StudentEntry student;
        
        String scheduletext = "Scheduled Students dropped from course:\n";
        String waitlisttext = "waitlisted students dropped from course:\n";
        
        for (ScheduleEntry scheduled : scheduledstudents ) {
                student = StudentQueries.getStudent(scheduled.getStudentID());
                scheduletext += student.getLastname() + " ," + student.getFirstname() + student.getStudentID() + "\n";
        }
        for (ScheduleEntry waitlisted : waitlistedstudents ) {
                student = StudentQueries.getStudent(waitlisted.getStudentID());
                waitlisttext += student.getLastname()+ " ," + student.getFirstname() + student.getStudentID() + "\n";
        }
        
        DropCourseTextArea.append(scheduletext + "\n");
        DropCourseTextArea.append(waitlisttext);
        ScheduleQueries.dropScheduleByCourse(currentSemester, currentCourse);
        CourseQueries.dropCourse(currentSemester, currentCourse);
        rebuildCourseComboBoxes();
    }//GEN-LAST:event_DropCourseButtonActionPerformed

    private void DropStudentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropStudentButtonActionPerformed
        DropStudentTextArea.setText("");
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        String currentStudentID = students.get(DropStudentComboBox.getSelectedIndex()).getStudentID();
        
        DropStudentTextArea.append((String)DropStudentComboBox.getSelectedItem() + " has been dropped form the list of students\n");
        
        ArrayList<String> semesters = SemesterQueries.getSemesterList();
        
        //removes the schedules for that student
        for (String semester : semesters) {
            DropStudentTextArea.append("For Semester: " + semester + " \n");
            ArrayList<ScheduleEntry> Schedules = ScheduleQueries.getScheduleByStudent(semester, currentStudentID);
            
            for (ScheduleEntry studentSchedule : Schedules) {
                ScheduleQueries.dropStudentScheduleByCourse(semester, currentStudentID ,studentSchedule.getCoursecode() );

                if (studentSchedule.getStatus().equals("S") ) {
                    DropStudentTextArea.append((String)DropStudentComboBox.getSelectedItem() + " has been dropped from " + studentSchedule.getCoursecode() + "\n");
                } else {
                    DropStudentTextArea.append((String)DropStudentComboBox.getSelectedItem() + " has been dropped from the waitlist for " + studentSchedule.getCoursecode() + "\n");
                }
                
                //add the next student to the course from the waitlist
                ArrayList<ScheduleEntry> waitlistedstudents = ScheduleQueries.getWaitlistedStudentsByCourse(currentSemester, studentSchedule.getCoursecode());

                if (waitlistedstudents.size() > 0) {
                    StudentEntry newstudent = StudentQueries.getStudent(waitlistedstudents.get(0).getStudentID());
                    ScheduleQueries.updateScheduleEntry(semester, waitlistedstudents.get(0));
                    DropStudentTextArea.append(newstudent.getLastname() + " " + newstudent.getFirstname() + " has been removed from waitlist and enrolled into " + studentSchedule.getCoursecode() + "\n");
                }
            }
        
        }
        StudentQueries.dropStudent(currentStudentID);
        DropStudentTextArea.append("\n" + (String)DropStudentComboBox.getSelectedItem() +   " has been dropped from all comboboxes");
        rebuildStudentComboBoxes();
    }//GEN-LAST:event_DropStudentButtonActionPerformed

